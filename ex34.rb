# The third (3rd) animal. ~ordinal
puts animals[2] == 'peacock'

# The first (1st) animal.
puts animals[0] == 'bear'

#The animal at 3.
puts animals[3] == 'kangaroo'

# The fifth (5th) animal.
puts animals[4] == 'whale'

